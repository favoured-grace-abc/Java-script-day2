const score = [90, 80, 70, 60, 50]
for (i of score){
    if (i >= 90) {
        console.log(`${i}: Grade-A`);
    }
    else if (i >= 80) {
        console.log(`${i}: Grade-B`);
    }
    else if (i >= 70) {
        console.log(`${i}: Grade-C`);
    }
    else if (i >= 60) {
        console.log(`${i}: Grade-D`);
    }
    else {
        console.log(`${i}: Grade-F`);
    }

}

      
    